# Создаем переменную example
example = "Привет"

# Выводим на консоль первый символ
print(example[0])

# Выводим на консоль последний символ
print(example[-1])

# Выводим на консоль вторую половину строки
print(example[3:])

# Выводим на консоль строку в обратном порядке
print(example[::-1])

# Выводим на консоль каждый второй символ строки
print(example[::2])